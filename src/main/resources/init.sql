    OID INT PRIMARY KEY,
    CURRENCY_NAME VARCHAR(20),
    CURRENCY_CODE VARCHAR(5),
    SYMBOL VARCHAR(5),
    RATE VARCHAR(20),
    DESCRIPTION VARCHAR(500),
    CREATED_TIMESTAMP BIGINT,
